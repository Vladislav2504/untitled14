public class Coordinates {

    private double x;
    private double y;
    private double z;

    public Coordinates(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }

    public Coordinates(double x, double y){
        this.x = x;
        this.y = y;
    }
    public double getX1() {
        return x;
    }

    public void setX1(double x) {
        this.x = x;
    }

    public double getY1() {
        return y;
    }

    public void setY1(double y) {
        this.y = y;
    }
}
